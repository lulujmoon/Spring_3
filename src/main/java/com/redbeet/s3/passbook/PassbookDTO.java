package com.redbeet.s3.passbook;

public class PassbookDTO {
	
	private int num;
	private String name;
	private double irate;
	private String openable;
	
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public double getIrate() {
		return irate;
	}
	public void setIrate(double irate) {
		this.irate = irate;
	}
	public String getOpenable() {
		return openable;
	}
	public void setOpenable(String openable) {
		this.openable = openable;
	}
	
	

}
